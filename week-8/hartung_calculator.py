# create an "add" function with two paramenters
def add(a,b):
    return (a+b)

# create a "subtract" function with two parameters
def subtract(a,b):
    return (a-b)

# create a "divide" function with two parameters
def divide(a,b):
    return (a/b)

# call each function with a separate print statement
print(add(1,2))
print(subtract(4,1))
print(divide(8,2))